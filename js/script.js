// Fecha fija del evento: 15 de junio de 2025 a las 18:00
const eventoCuentaRegresiva = new Date("2025-06-15T18:00:00");

function actualizarCountdown() {
  const actual = new Date();
  const diferencia = eventoCuentaRegresiva - actual;

  if (diferencia <= 0) {
    document.getElementById("countdown").innerHTML = "¡El evento ha comenzado!";
    return;
  }

  const totalSegundos = Math.floor(diferencia / 1000);
  const dias = Math.floor(totalSegundos / (60 * 60 * 24));
  const horas = Math.floor((totalSegundos % (60 * 60 * 24)) / (60 * 60));
  const minutos = Math.floor((totalSegundos % (60 * 60)) / 60);

  document.getElementById("days").textContent = dias;
  document.getElementById("hours").textContent = horas;
  document.getElementById("minutes").textContent = minutos;
}

// Inicia y actualiza cada minuto
actualizarCountdown();
setInterval(actualizarCountdown, 60000);

